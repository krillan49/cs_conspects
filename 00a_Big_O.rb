# O(1) [Constant time / константное время] - сколько бы элементов не было добавилено в структуру данных (например в хеш), поиск всегда будет занимать одно и то же время(сразу найдет нужное).

# O(N) [linear time / линейное время] - для поиска элемента нам необходимо перебрать все элементы структуры данных (например массив). Чем больше элементов, тем поиск будет занимать линейно больше времени.
